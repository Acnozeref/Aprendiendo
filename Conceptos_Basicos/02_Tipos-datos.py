# Ejemplos de los tipos de datos fundamentales en Python:
# - str  → cadenas de texto
# - int  → números enteros
# - float → números decimales
# - bool → valores booleanos (True/False)

texto = "Hola mundo"       # str
entero = 42                # int
decimal = 3.14             # float
booleano = True            # bool

print(type(texto))
print(type(entero))
print(type(decimal))
print(type(booleano))


