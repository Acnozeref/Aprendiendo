# --- OPERADORES DE COMPARACIÓN EN PYTHON ---
# (Versión profesional + explicación educativa)

# --------------------------------------------------
#  PRO (comentario profesional)
# --------------------------------------------------
# Los operadores de comparación permiten evaluar relaciones entre valores.
# Devuelven siempre un resultado booleano: True o False.
# Operadores comunes:
# ==  igualdad
# !=  diferente
# >   mayor que
# <   menor que
# >=  mayor o igual que
# <=  menor o igual que


# Declaración de variables
a = 10
b = 5

# Comparaciones
print(a == b)  # Igualdad
print(a != b)  # Diferente
print(a > b)   # Mayor que
print(a < b)   # Menor que
print(a >= b)  # Mayor o igual que
print(a <= b)  # Menor o igual que


# --------------------------------------------------
#  EDUCATIVO (comentario nivel aprendizaje)
# --------------------------------------------------
# Estos operadores comparan dos valores y devuelven True o False.
# Ejemplo: 10 > 5 devuelve True, porque 10 es mayor que 5.
# Son esenciales para condiciones (if, elif, else) y para validaciones.
# Cada comparación evalúa una relación distinta entre los valores.
