# Ejemplos básicos de operadores aritméticos:
# +  suma
# -  resta
# *  multiplicación
# /  división con decimales
# // división entera
# %  residuo o módulo
# ** potencia

# Declaración de variables
a = 10
b = 3

# Operaciones
print(a + b)  # Suma
print(a - b)  # Resta
print(a * b)  # Multiplicación
print(a / b)  # División (resultado decimal)
print(a // b) # División entera (trunca decimales)
print(a % b)  # Módulo: residuo de la división
print(a ** b) # Potencia: a elevado a b


# --------------------------------------------------
#  EDUCATIVO (comentario nivel aprendizaje)
# --------------------------------------------------
# En Python puedes hacer operaciones matemáticas fácilmente:
# 
# - a + b → suma
# - a - b → resta
# - a * b → multiplicación
# - a / b → devuelve decimales (división normal)
# - a // b → devuelve solo la parte entera (divide y trunca)
# - a % b → devuelve el residuo de la división
# - a ** b → "a elevado a b"
# 
# Estos operadores son la base para cálculos, lógica, ciclos, validaciones
# y prácticamente cualquier programa que escribas.
