# Estructura básica de una condición en Python.
# if → ejecuta el bloque si la condición es verdadera.
# else → ejecuta el bloque alterno si la condición es falsa.

edad = 18

if edad >= 18:
    print("Eres mayor de edad")
else:
    print("Eres menor de edad")


